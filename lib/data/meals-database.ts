/**
 * Get random meal of specific type and price tier
 */
export function getRandomMeal(type: MealType, priceTier: PriceTier): Meal {
  const meals = MEALS_DATABASE[type].filter((meal) => meal.price === priceTier)
  return meals[Math.floor(Math.random() * meals.length)]
}

/**
 * Get meals matching specific macros (approximation)
 */
export function getMealMatchingMacros(
  type: MealType,
  targetProtein: number,
  targetCarbs: number,
  priceTier: PriceTier
): Meal {
  const meals = MEALS_DATABASE[type].filter((meal) => meal.price === priceTier)

  // Sort by how close the meal is to target macros
  meals.sort((a, b) => {
    const diffA = Math.abs(a.protein - targetProtein) + Math.abs(a.carbs - targetCarbs)
    const diffB = Math.abs(b.protein - targetProtein) + Math.abs(b.carbs - targetCarbs)
    return diffA - diffB
  })

  return meals[0]
}
